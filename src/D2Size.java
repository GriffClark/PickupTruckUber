
public class D2Size {
	private int x;
	private int y;
	
	public D2Size(int x, int y) {
		
	}
	
	public D2Size() {
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	public String toString() {
		return x + "x" + y;
	}
}
